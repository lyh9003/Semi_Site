/** KST 날짜 문자열(YYYY-MM-DD)이 휴장일(주말 또는 공휴일)이면 true */
export function isKRMarketClosed(kstDate: string): boolean {
  const day = new Date(`${kstDate}T12:00:00+09:00`).getDay(); // 0=일, 6=토
  return day === 0 || day === 6 || KR_HOLIDAYS.has(kstDate);
}
